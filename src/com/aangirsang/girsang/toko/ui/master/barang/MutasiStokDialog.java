    public Barang showDialog(Barang b,String title, String status){
        operator = status;
        setTitle(title);
        Clear();
        if(b!=null){
            barang = new Barang();
            barang = b;
            stokToko = b.getStokToko();
            stokGudang = b.getStokGudang();
        } else{
            stokToko = 0;
            stokGudang = 0;
        }
        pack();
        setLocationRelativeTo(null);
        setVisible(true);
        return this.barang;
    }
    private void Clear(){
       jspStokToko.setValue(0);
       jspStokGudang.setValue(0);
    }

