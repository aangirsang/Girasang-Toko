    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if ("".equals(txtGolongan.getText())) {
            JOptionPane.showMessageDialog(this, "Golongan Belum Terisi");
            txtGolongan.requestFocus();
        } else {
            if (golonganBarang == null) {
                golonganBarang = new GolonganBarang();
            }
            golonganBarang.setId(txtId.getText());
            golonganBarang.setGolonganBarang(txtGolongan.getText());
            dispose();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

