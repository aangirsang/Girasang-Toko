    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if("Tambah".equals(operator)){
            stokToko = stokToko + (Integer) jspStokToko.getValue();
            stokGudang = stokGudang + (Integer) jspStokGudang.getValue();
        }else if("Kurang".equals(operator)){
            stokToko = stokToko - (Integer) jspStokToko.getValue();
            stokGudang = stokGudang - (Integer) jspStokGudang.getValue();
        }
        barang.setStokToko(stokToko);
        barang.setStokGudang(stokGudang);
        
        dispose();
    }//GEN-LAST:event_btnSimpanActionPerformed

